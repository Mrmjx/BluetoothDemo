package com.mo.bluetoothdemo;

public enum Code {

     /**
      * 连接成功
      */
     CONNECTION_SUCCESS,

     /**
      * 连接失败
      */
     CONNECTION_FAILTH,

     /**
      * 连接超时
      */
     CONNECTION_TIMEOUT,

     /**
      * 没有搜到该设备
      */
     DEVICE_SHEARCH_FAILURE,

     /**
      * 断开连接
      */
     DISCONNECT,

     /**
      * 无效的蓝牙
      */
     INVALID_MAC,

     /**
      * 找到设备的蓝牙，开始连接
      */
     CONNECTING,



 
}